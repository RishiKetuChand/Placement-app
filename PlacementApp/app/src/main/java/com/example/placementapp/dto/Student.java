package com.example.placementapp.dto;

public class Student {
    String companyName;
    String companyLoc;
    String companyDate;
    String allowedDepartment;
    String studentName;
    String studentEmail;
    String studentPhoneNumber;
    String studentUsn;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getCompanyLoc() {
        return companyLoc;
    }

    public void setCompanyLoc(String companyLoc) {
        this.companyLoc = companyLoc;
    }

    public String getCompanyDate() {
        return companyDate;
    }

    public void setCompanyDate(String companyDate) {
        this.companyDate = companyDate;
    }

    public String getAllowedDepartment() {
        return allowedDepartment;
    }

    public void setAllowedDepartment(String allowedDepartment) {
        this.allowedDepartment = allowedDepartment;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public String getStudentEmail() {
        return studentEmail;
    }

    public void setStudentEmail(String studentEmail) {
        this.studentEmail = studentEmail;
    }

    public String getStudentPhoneNumber() {
        return studentPhoneNumber;
    }

    public void setStudentPhoneNumber(String studentPhoneNumber) {
        this.studentPhoneNumber = studentPhoneNumber;
    }

    public String getStudentUsn() {
        return studentUsn;
    }

    public void setStudentUsn(String studentUsn) {
        this.studentUsn = studentUsn;
    }



}
